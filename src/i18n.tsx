const TRANSLATIONS = { de, en } as const;

// ── Context ─────────────────────────────────────────────

type LangCtx = { lang: Lang; setLang: (l: Lang) => void; t: (key: keyof typeof de) => string };
const LangContext = createContext<LangCtx>({
  lang: "de",
  setLang: () => {},
  t: (k) => de[k],
});

export function LangProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    try { return (localStorage.getItem("lang") as Lang) ?? "de"; } catch { return "de"; }
  });
  const setLang = (l: Lang) => {
    try { localStorage.setItem("lang", l); } catch {}
    setLangState(l);
  };
  const t = (key: keyof typeof de) => TRANSLATIONS[lang][key] ?? de[key];
  return <LangContext.Provider value={{ lang, setLang, t }}>{children}</LangContext.Provider>;
}

export const useLang = () => useContext(LangContext);
